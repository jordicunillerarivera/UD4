
public class UD4_4APP {

	public static void main(String[] args) {
		
		int n = 20;
		
		System.out.println("Valor inicial de N = " + n);
		
		n = n + 77;
		
		System.out.println("N + 77 = " + n);
		
		n = n - 3;
		
		System.out.println("N - 3 = " + n);
		
		n = n * 2;
		
		System.out.println("N * 2 = " + n);

	}

}
